/**
 * Advanced Pay JavaScript SDK v1.0
 * Embeddable checkout designed for massive scale and instant integrations.
 */
class AdvancedPay {
  constructor(publishableKey, options = {}) {
    if (!publishableKey || !publishableKey.startsWith('pk_')) {
      console.error('AdvancedPay: Invalid Publishable Key. Key must start with "pk_".');
      return;
    }
    this.key = publishableKey;
    this.iframeId = 'advancedpay-checkout-frame';
    this.appUrl = options.appUrl || 'http://localhost:3001';
    this.onSuccess = options.onSuccess || function () {};
    this.onError = options.onError || function () {};
    this.onClose = options.onClose || function () {};

    this._setupMessageListener();
  }

  /**
   * Initializes the checkout overlay utilizing a secure session token.
   * @param {string} sessionToken - The token generated by the merchant backend.
   */
  openCheckout(sessionToken) {
    if (!sessionToken) {
      console.error('AdvancedPay: sessionToken is required to open checkout.');
      return;
    }

    if (document.getElementById(this.iframeId)) {
      console.warn('AdvancedPay: Checkout is already open.');
      return;
    }

    const overlay = document.createElement('div');
    overlay.id = this.iframeId + '-container';
    Object.assign(overlay.style, {
      position: 'fixed',
      top: '0',
      left: '0',
      width: '100vw',
      height: '100vh',
      backgroundColor: 'rgba(0, 0, 0, 0.5)',
      backdropFilter: 'blur(8px)',
      zIndex: '999999',
      display: 'flex',
      alignItems: 'center',
      justifyContent: 'center',
      opacity: '0',
      transition: 'opacity 0.3s ease',
    });

    const iframe = document.createElement('iframe');
    iframe.id = this.iframeId;
    iframe.src = `${this.appUrl}/checkout/embed?session=${sessionToken}&pk=${this.key}`;
    Object.assign(iframe.style, {
      width: '100%',
      maxWidth: '430px', /* Mobile app width limit */
      height: '650px',
      maxHeight: '90vh',
      border: 'none',
      borderRadius: '24px',
      boxShadow: '0 25px 50px -12px rgba(0, 0, 0, 0.4)',
      transform: 'translateY(20px) scale(0.95)',
      transition: 'transform 0.4s cubic-bezier(0.16, 1, 0.3, 1)',
      backgroundColor: 'transparent',
    });

    overlay.appendChild(iframe);
    document.body.appendChild(overlay);
    
    // Lock body scroll
    this._originalOverflow = document.body.style.overflow;
    document.body.style.overflow = 'hidden';

    // Animate In
    requestAnimationFrame(() => {
      overlay.style.opacity = '1';
      iframe.style.transform = 'translateY(0) scale(1)';
    });
  }

  closeCheckout() {
    const overlay = document.getElementById(this.iframeId + '-container');
    if (!overlay) return;

    // Animate out
    overlay.style.opacity = '0';
    const iframe = document.getElementById(this.iframeId);
    if (iframe) {
      iframe.style.transform = 'translateY(20px) scale(0.95)';
    }

    setTimeout(() => {
      if (document.body.contains(overlay)) {
        document.body.removeChild(overlay);
      }
      document.body.style.overflow = this._originalOverflow;
    }, 300);
    
    this.onClose();
  }

  _setupMessageListener() {
    window.addEventListener('message', (event) => {
      // Security check (ideally validate event.origin matches this.appUrl)
      if (!event.data || typeof event.data !== 'object') return;
      if (event.data.source !== 'advancedpay-sdk') return;

      const { type, payload } = event.data;

      switch (type) {
        case 'PAYMENT_SUCCESS':
          this.onSuccess(payload);
          this.closeCheckout();
          break;
        case 'PAYMENT_ERROR':
          this.onError(payload);
          // We can optionally keep it open to let the user try again
          break;
        case 'CLOSE_CHECKOUT':
          this.closeCheckout();
          break;
      }
    }, false);
  }
}

// Make globally available
window.AdvancedPay = AdvancedPay;
